package com.note.model;

public class SubTitle {
	private int id;
	
	private String subtitle;
	
	private String subtitlestatus;
	
	private String relation;
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the subtitle
	 */
	public String getSubtitle() {
		return subtitle;
	}

	/**
	 * @param subtitle the subtitle to set
	 */
	public void setSubtitle(String subtitle) {
		this.subtitle = subtitle;
	}

	/**
	 * @return the subtitlestatus
	 */
	public String getSubtitlestatus() {
		return subtitlestatus;
	}

	/**
	 * @param subtitlestatus the subtitlestatus to set
	 */
	public void setSubtitlestatus(String subtitlestatus) {
		this.subtitlestatus = subtitlestatus;
	}

	/**
	 * @return the relation
	 */
	public String getRelation() {
		return relation;
	}

	/**
	 * @param relation the relation to set
	 */
	public void setRelation(String relation) {
		this.relation = relation;
	}

	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}

	/**
	 * @param remark the remark to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}

	private String remark;
	

}
