package com.note.model;


public class UserInfo {

	private int id;
	
	private String username;
	
	private String password;
	
	private String createtime;
	
	private String lastlogintime;
	
	private String currentlogintime;
	
	private String isvalid;
	
	private String loginouttime;
	
	private String remark;
	
	private String role;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the username
	 */
	public String getUsername() {
		return username;
	}

	/**
	 * @param username the username to set
	 */
	public void setUsername(String username) {
		this.username = username;
	}

	/**
	 * @return the password
	 */
	public String getPassword() {
		return password;
	}

	/**
	 * @param password the password to set
	 */
	public void setPassword(String password) {
		this.password = password;
	}

	/**
	 * @return the createtime
	 */
	public String getCreatetime() {
		return createtime;
	}

	/**
	 * @param createtime the createtime to set
	 */
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}

	/**
	 * @return the lastlogintime
	 */
	public String getLastlogintime() {
		return lastlogintime;
	}

	/**
	 * @param lastlogintime the lastlogintime to set
	 */
	public void setLastlogintime(String lastlogintime) {
		this.lastlogintime = lastlogintime;
	}

	/**
	 * @return the currentlogintime
	 */
	public String getCurrentlogintime() {
		return currentlogintime;
	}

	/**
	 * @param currentlogintime the currentlogintime to set
	 */
	public void setCurrentlogintime(String currentlogintime) {
		this.currentlogintime = currentlogintime;
	}

	/**
	 * @return the isvalid
	 */
	public String getIsvalid() {
		return isvalid;
	}

	/**
	 * @param isvalid the isvalid to set
	 */
	public void setIsvalid(String isvalid) {
		this.isvalid = isvalid;
	}

	/**
	 * @return the loginouttime
	 */
	public String getLoginouttime() {
		return loginouttime;
	}

	/**
	 * @param loginouttime the loginouttime to set
	 */
	public void setLoginouttime(String loginouttime) {
		this.loginouttime = loginouttime;
	}

	/**
	 * @return the remark
	 */
	public String getRemark() {
		return remark;
	}

	/**
	 * @param remark the remark to set
	 */
	public void setRemark(String remark) {
		this.remark = remark;
	}

	/**
	 * @return the role
	 */
	public String getRole() {
		return role;
	}

	/**
	 * @param role the role to set
	 */
	public void setRole(String role) {
		this.role = role;
	}
	
}